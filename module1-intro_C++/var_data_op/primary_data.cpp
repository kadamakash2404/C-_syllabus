#include<iostream>

using namespace std;


int a = 10;
char c = 'a';
bool val = true;
float rain = 45.95;
double mark = 4898.1685;



int main()
{
    cout<<"int a = "<<a<<" & size = "<<sizeof(int)<<endl;
    cout<<"char c = "<<c<<" & size = "<<sizeof(char)<<endl;
    cout<<"bool val = "<<val<<" & size = "<<sizeof(bool)<<endl;
    cout<<"float rain = "<<rain<<" & size = "<<sizeof(float)<<endl;
    cout<<"double mark = "<<mark<<" & size = "<<sizeof(double)<<endl;
    return 0;
}